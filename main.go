//nolint:depguard
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"io"
	"log/slog"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

const (
	filePerms          = 0o600
	defaultOutFileName = "constants.gen.go"
)

type openAPISpec struct {
	XConstants map[string]map[string]any `yaml:"x-constants"` //nolint:tagliatelle
}

type typedConstant struct {
	name    string
	goType  string
	value   any
	fmtVerb string
}

func main() {
	os.Exit(run(os.Args[1:], os.Stderr))
}

func run(args []string, logOut io.Writer) int {
	logger := slog.New(slog.NewTextHandler(logOut, nil))

	fs := flag.NewFlagSet("oapixconstgen", flag.ContinueOnError)
	fs.SetOutput(logOut)

	specPath := fs.String("spec", "", "path to OpenAPI spec file")
	outDir := fs.String("out", ".", "output directory")
	outFile := fs.String("file", defaultOutFileName, "output file name")
	pkg := fs.String("pkg", "api", "package name")

	if err := fs.Parse(args); err != nil {
		return 1
	}

	if *specPath == "" {
		logger.Error("spec path is required", "usage", "-spec=path/to/api.yml")

		return 1
	}

	data, err := os.ReadFile(*specPath)
	if err != nil {
		logger.Error("failed to read spec file", "error", err)

		return 1
	}

	var spec openAPISpec
	if err := yaml.Unmarshal(data, &spec); err != nil {
		logger.Error("failed to parse spec file", "error", err)

		return 1
	}

	constants := collectConstants(spec)
	if len(constants) == 0 {
		logger.Info("no x-constants found in spec, skipping generation")

		return 0
	}

	code, err := generateCode(*pkg, constants)
	if err != nil {
		logger.Error("failed to generate code", "error", err)

		return 1
	}

	outPath := filepath.Join(*outDir, *outFile)
	if err := os.WriteFile(outPath, code, filePerms); err != nil {
		logger.Error("failed to write output file", "error", err)

		return 1
	}

	logger.Info("constants generated", "path", outPath)

	return 0
}

func collectConstants(spec openAPISpec) []typedConstant {
	var constants []typedConstant

	typeFormats := map[string]string{
		"int":     "%d",
		"int8":    "%d",
		"int16":   "%d",
		"int32":   "%d",
		"int64":   "%d",
		"uint":    "%d",
		"uint8":   "%d",
		"uint16":  "%d",
		"uint32":  "%d",
		"uint64":  "%d",
		"float32": "%v",
		"float64": "%v",
		"string":  "%q",
		"bool":    "%t",
	}

	for goType, values := range spec.XConstants {
		fmtVerb, ok := typeFormats[goType]
		if !ok {
			continue
		}

		for name, value := range values {
			constants = append(constants, typedConstant{
				name:    name,
				goType:  goType,
				value:   value,
				fmtVerb: fmtVerb,
			})
		}
	}

	sort.Slice(constants, func(i, j int) bool {
		return constants[i].name < constants[j].name
	})

	return constants
}

func generateCode(pkg string, constants []typedConstant) ([]byte, error) {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by oapixconstgen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", pkg)
	buf.WriteString("const (\n")

	for _, c := range constants {
		goName := toExportedName(c.name)
		fmtStr := fmt.Sprintf("\t%%s %s = %s\n", c.goType, c.fmtVerb)
		fmt.Fprintf(&buf, fmtStr, goName, c.value)
	}

	buf.WriteString(")\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("failed to format generated code: %w", err)
	}

	return formatted, nil
}

func toExportedName(name string) string {
	parts := strings.Split(name, "_")
	for i, part := range parts {
		if len(part) == 0 {
			continue
		}

		parts[i] = strings.ToUpper(part[:1]) + part[1:]
	}

	return strings.Join(parts, "")
}
